class Solution {
public:
    int uniquePathsWithObstacles(vector<vector<int>>& obstacleGrid) {
        if(!obstacleGrid.size())
            return 0;
        int m = obstacleGrid.size();
        int n = obstacleGrid[0].size();
        long long table[m][n];
        int i, j;
        for(i = m - 1; i >= 0; i--)
            if(obstacleGrid[i][n-1])
                break;
            else
                table[i][n-1] = 1;
        for(; i >= 0; i--)
            table[i][n-1] = 0;
        for(j = n - 1; j >= 0; j--)
            if(obstacleGrid[m-1][j])
                break;
            else
                table[m-1][j] = 1;
        for(; j >= 0; j--)
            table[m-1][j] = 0;
        for(i = m - 2; i >= 0; i--)
            for(j = n - 2; j >= 0; j--){
                if(obstacleGrid[i][j])
                    table[i][j] = 0;
                else{
                    if(obstacleGrid[i+1][j] && obstacleGrid[i][j+1])
                        table[i][j] = 0;
                    else if(obstacleGrid[i+1][j])
                        table[i][j] = table[i][j+1];
                    else if(obstacleGrid[i][j+1])
                        table[i][j] = table[i+1][j];
                    else
                        table[i][j] = table[i][j+1] + table[i+1][j];
                }
            }
        return table[0][0];
    }
};

